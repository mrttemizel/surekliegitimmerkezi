            Antalya Bilim Üniversitesi Sürekli Eğitim Uygulama ve Araştırma Merkezi (“ABÜSEM”) olarak ABÜSEM web sitesini ziyaret edenlerin, ABÜSEM aday işlemleri panelini kullanan kişilerin ve bizimle elektronik olarak iletişim kuran herkesin kişisel verilerine saygı gösteriyoruz. ABÜSEM olarak eğitim ve hizmetlerimizden faydalanan kişiler dahil, ABÜSEM ile ilişkili tüm gerçek kişilere ait her türlü kişisel verilerin 6698 sayılı Kişisel Verilerin Korunması Kanunu (“KVK Kanunu”)’na uygun olarak işlenmesini ve muhafazasını sağlamak için gerekli tedbirleri alıyoruz. İşbu Kişisel Verileri Koruma Politikası, Kişisel Verilen İşlenmesine İlişkin Aydınlatma Metni, web sitemizi kullanırken kullanıcılara ilişkin ne tür bilgilerin ABÜSEM tarafından alındığını, bunların nasıl kullanıldığını ve korunduğunu açıklamaktadır.
            ABÜSEM, KVK Kanunu’na uymakla yükümlüdür ve faaliyet süreçlerinde işlenen tüm kişisel veriler bu kanun kapsamındadır.
            Bu sorumluluğumuzun farkında olarak, KVK Kanunu’nda tanımlı şekli ile “Veri Sorumlusu” sıfatıyla, kişisel verilerinizi aşağıda açıklandığı şekliyle ve mevzuat tarafından belirlenen sınırlar çerçevesinde işlemekteyiz ve kişisel verilerin korunması hakkında gerekli önlemleri almaktayız.
            1. Kişisel Verilerin İşlenmesinde Genel İlkeler
            ABÜSEM, kişisel verilerin işlenmesinde KVK Kanunu’nda ortaya koymuş olduğu genel ilkelere uygun davranmaktadır. Kişisel verilerin işlenmesinde genel ilkelerimiz şu şekildedir;
            1) Hukuka ve dürüstlük kurallarına uygun olma,
            2) Doğru ve gerektiğinde güncel olma,
            3) Belirli, açık ve meşru amaçlar için işlenme,
            4) İşlendikleri amaçla bağlantılı, sınırlı ve ölçülü olma,
            5) İlgili mevzuatta öngörülen veya işlendikleri amaç için gerekli olan süre kadar muhafaza etme.
            2. İşlenen Kişisel Verilerin Türleri
            İşbu Kişisel Verilen İşlenmesine İlişkin Aydınlatma Metni’ nde, “kişisel verileriniz” kimliğinizi veya üçüncü bir kişinin kimliğini belirten ya da belirlenebilir kılan her türlü veriyi ifade eder.
            İşin doğası gereği adınız ve soyadınızı, TC Kimlik numaranızı, adresinizi, e-postanızı, telefon numaranızı, sunmanız halinde kurum kartınızı, pasaportunuzda bulunan bilgilerinizi, imzanızı, sunmanız halinde vize bilgilerinizi, IP adresinizi ve ABÜSEM’ de başvurduğunuz önceki eğitim ve sınav bilgilerini içerir. E-posta adresi ve telefon numarası sizi bilgilendirmek için toplanmaktadır. Bilgilerinizi eksik/hatalı yazarak başvuru yaparsanız size bilgilendirme yapılamamasından dolayı sorumluluk kabul edilmez.
            3. Kişisel Verilerin Toplanması, İşlenmesi ve İşleme Amaçları
            Kişisel verileriniz, ABÜSEM tarafından sağlanan hizmet ve ticari faaliyetlere bağlı olarak değişkenlik gösterebilmekle birlikte; otomatik ya da otomatik olmayan yöntemlerle, Antalya Bilim Üniversitesi birimleri, internet sitesi, sosyal medyası, mobil uygulamalar ve benzeri vasıtalarla sözlü, yazılı ya da elektronik olarak toplanabilecektir.
            ABÜSEM’in sunduğu hizmetlerden yararlandığınız sürece kişisel verileriniz işbu metinde belirtilen amaçlarla işlenebilecektir.

